// JS Variables
/* 
1. A JavaScript variable is simply a name of storage location.
2. Name must start with a letter (a to z or A to Z), underscore( _ ), or dollar( $ ) sign.
3. After first letter we can use digits (0 to 9), for example value1.
4. JavaScript variables are case sensitive, for example x and X are different variables.
*/

var x = 10;
var y = 20;
var z = x + y;
